void CLayoutMoreWnd::OnTimer(UINT nIDEvent)
{
	// TODO: Add your message handler code here and/or call default
	//四秒对窗口没有操作，关闭窗口
	if(nIDEvent == TIMER_EVENT_CLOSEARRWND)
	{
		m_uiIdleTime += TIMER_EVENT_ELAPSE;

		if(m_uiIdleTime >= TIMER_EVENT_CLOSEELAPSE)
		{
			POINT pt;
			GetCursorPos(&pt);

			CRect rectWindow(0,0,0,0);

			GetWindowRect(&rectWindow);

			if(rectWindow.PtInRect(pt))
			{
				m_uiIdleTime = 0;
			}
            else
			{
				ShowWindow(SW_HIDE);
				m_uiIdleTime = 0;
			}
		}
	}

	CCustomDrawBlackWnd::OnTimer(nIDEvent);
}

/*************************************************
Function:    	SetCloseTimer
Description:	设置时钟
Input:		
Output:      	
Return:			
*************************************************/
bool CLayoutMoreWnd::SetCloseTimer(void)
{
	if(m_uiCloseTimer != 0)
	{
		KillColseTimer();
	}

	m_uiIdleTime = 0;
	m_uiCloseTimer = SetTimer(TIMER_EVENT_CLOSEARRWND,TIMER_EVENT_ELAPSE,NULL);

	return true;
}

/*************************************************
Function:    	KillColseTimer
Description:	停止时钟
Input:		
Output:      	
Return:			
*************************************************/
void CLayoutMoreWnd::KillColseTimer(void)
{
	if(m_uiCloseTimer != 0)
	{
		KillTimer(TIMER_EVENT_CLOSEARRWND);
		m_uiCloseTimer = 0;
	}

	m_uiIdleTime = 0;
}

